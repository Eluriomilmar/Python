numero = input("Insira o número da categoria, deve ser um número inteiro: ")
if numero == 1:
    print("O preço da categoria %6.2f é de 10 Reais" % numero)
elif numero == 2:
    print("O preço da categoria %6.2f é de 18 Reais" % numero)
elif numero == 3:
    print("O preço da categoria %6.2f é de 23 Reais" % numero)
elif numero == 4:
    print("O preço da categoria %6.2f é de 26 Reais" % numero)
elif numero == 5:
    print("O preço da categoria %6.2f é de 31 Reais" % numero)
else:
    print("Categoria inválida!")